const skillsGeneratedHeader = "# Generated by mtclaw bridge install-agents — do not edit manually"

// SDLCFrameworkSkill returns the SDLC Framework skill content for Claude Code.
// Content must stay under 5000 chars (Claude Code skill budget).
func SDLCFrameworkSkill() string {
	return `# SDLC Framework — Developer Reference

## Gate Definitions

| Gate | Name | Required Evidence | Approver |
|------|------|-------------------|----------|
| G-Sprint | Sprint Gate | Tests pass, code reviewed, no P0 bugs | Tech Lead |
| G0.1 | Problem Definition | BRD with stakeholder sign-off | PM |
| G0.2 | Solution Diversity | 3+ solution options evaluated | Architect |
| G1 | Market Validation | Competitive analysis, legal review | CPO |
| G2 | Design Ready | ADRs, API spec, data model | CTO |
| G3 | Ship Ready | 95%+ test coverage, security scan, load test | CTO + CPO |
| G4 | Internal Validation | 30-day pilot metrics | CEO |

## SOUL Delegation Rules

Each role has specific responsibilities. Do not cross role boundaries:

- **PM**: Requirements, user stories, prioritization. Does NOT write code.
- **Architect**: System design, ADRs, tech stack. Does NOT implement features.
- **Coder**: Implementation, tests, commits. Does NOT make design decisions.
- **Reviewer**: Code review, quality gates. Does NOT modify code directly.
- **Tester**: Test plans, QA validation. Does NOT fix bugs (reports them).
- **DevOps**: CI/CD, deployment, monitoring. Does NOT change business logic.
- **CTO**: Architecture approval, security review. Final technical authority.
- **CPO**: Product strategy, UX validation. Final product authority.

## Evidence Requirements

Every gate requires evidence stored in the Evidence Vault:

1. **Code changes**: Git diff + commit hash
2. **Test results**: Test output + coverage report
3. **Reviews**: Reviewer ID + approval timestamp
4. **Deployments**: Deploy log + health check result
5. **Security**: SAST scan output + vulnerability count

## Key Rules

- Zero Mock Policy: No TODO/placeholder code. All implementations production-ready.
- AGPL Containment: Access MinIO/Grafana via HTTP API only, never import SDKs.
- Performance Budget: API p95 < 100ms, dashboard load < 1s.
- File Naming: Python snake_case (max 50 chars), TypeScript camelCase/PascalCase.
- Commit Format: feat|fix|refactor|test|docs(scope): description
- Branch Strategy: main (protected) + feature branches, squash merge only.
`
}

// InstallSkillsResult reports what happened during skill installation.
type InstallSkillsResult struct {
	Installed int
	Updated   int
	Skipped   int
}

// InstallSkills generates .claude/skills/ directory with SDLC Framework skill.
// Files with the generated header are updated; user-created files are skipped.
func InstallSkills(projectPath string, force bool) (*InstallSkillsResult, error) {
	skillDir := filepath.Join(projectPath, ".claude", "skills", "sdlc-framework")
	if err := os.MkdirAll(skillDir, 0755); err != nil {
		return nil, fmt.Errorf("create skills dir: %w", err)
	}

	result := &InstallSkillsResult{}
	skillFile := filepath.Join(skillDir, "SKILL.md")
	content := skillsGeneratedHeader + "\n\n" + SDLCFrameworkSkill()

	if existingData, readErr := os.ReadFile(skillFile); readErr == nil {
		existingContent := string(existingData)
		if !strings.HasPrefix(existingContent, skillsGeneratedHeader) && !force {
			result.Skipped++
			return result, nil
		}
		if !force {
			existingHash := hashString(string(existingData))
			newHash := hashString(content)
			if existingHash == newHash {
				result.Skipped++
				return result, nil
			}
		}
		result.Updated++
	} else {
		result.Installed++
	}

	if err := os.WriteFile(skillFile, []byte(content), 0644); err != nil {
		return nil, fmt.Errorf("write skill file: %w", err)
	}

	return result, nil
}

// SkillContentSize returns the character count of the SDLC Framework skill.
// Used in tests to verify it stays under the 5000 char budget.
func SkillContentSize() int {
	return len(SDLCFrameworkSkill())
}
