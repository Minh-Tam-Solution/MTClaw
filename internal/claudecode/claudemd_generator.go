package claudecode

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

const claudeMDGeneratedHeader = "# Generated by mtclaw bridge init-project — do not edit manually"

// ProjectProfile captures detected project characteristics for CLAUDE.md generation.
type ProjectProfile struct {
	Name       string
	Language   string // "go", "python", "typescript", "mixed"
	BuildCmd   string
	TestCmd    string
	GitRemote  string
	HasDocker  bool
	HasMakefile bool
}

// DetectProjectProfile scans a project directory for language, build/test commands.
func DetectProjectProfile(projectPath string) *ProjectProfile {
	profile := &ProjectProfile{
		Name: filepath.Base(projectPath),
	}

	profile.GitRemote = normalizeGitRemote(projectPath)

	// Detect language and build/test commands
	if fileExists(filepath.Join(projectPath, "go.mod")) {
		profile.Language = "go"
		profile.BuildCmd = "go build ./..."
		profile.TestCmd = "go test ./... -v -count=1"
	} else if fileExists(filepath.Join(projectPath, "package.json")) {
		profile.Language = "typescript"
		profile.BuildCmd = "npm run build"
		profile.TestCmd = "npm test"
	} else if fileExists(filepath.Join(projectPath, "pyproject.toml")) || fileExists(filepath.Join(projectPath, "setup.py")) {
		profile.Language = "python"
		profile.BuildCmd = "pip install -e ."
		profile.TestCmd = "pytest -v"
	} else {
		profile.Language = "mixed"
	}

	// Override with Makefile targets if present
	if fileExists(filepath.Join(projectPath, "Makefile")) {
		profile.HasMakefile = true
		makefile, _ := os.ReadFile(filepath.Join(projectPath, "Makefile"))
		content := string(makefile)
		if strings.Contains(content, "build:") {
			profile.BuildCmd = "make build"
		}
		if strings.Contains(content, "test:") {
			profile.TestCmd = "make test"
		}
	}

	profile.HasDocker = fileExists(filepath.Join(projectPath, "Dockerfile")) ||
		fileExists(filepath.Join(projectPath, "docker-compose.yml")) ||
		fileExists(filepath.Join(projectPath, "docker-compose.yaml"))

	return profile
}

// GenerateClaudeMD creates the CLAUDE.md content from a project profile.
// Concise format (under 100 lines per Claude Code best practice).
func GenerateClaudeMD(profile *ProjectProfile) string {
	var sb strings.Builder

	sb.WriteString(claudeMDGeneratedHeader + "\n\n")
	sb.WriteString(fmt.Sprintf("# %s\n\n", profile.Name))

	// Build & Test
	sb.WriteString("## Build & Test\n\n")
	sb.WriteString("```bash\n")
	if profile.BuildCmd != "" {
		sb.WriteString(fmt.Sprintf("# Build\n%s\n\n", profile.BuildCmd))
	}
	if profile.TestCmd != "" {
		sb.WriteString(fmt.Sprintf("# Test\n%s\n", profile.TestCmd))
	}
	sb.WriteString("```\n\n")

	// Project Info
	sb.WriteString("## Project Info\n\n")
	sb.WriteString(fmt.Sprintf("- Language: %s\n", profile.Language))
	if profile.HasMakefile {
		sb.WriteString("- Build system: Makefile\n")
	}
	if profile.HasDocker {
		sb.WriteString("- Containerized: Docker\n")
	}
	if profile.GitRemote != "" {
		sb.WriteString(fmt.Sprintf("- Repository: %s\n", profile.GitRemote))
	}

	sb.WriteString("\n## Conventions\n\n")
	sb.WriteString("- Follow existing code style and patterns\n")
	sb.WriteString("- Write tests for new functionality\n")
	sb.WriteString("- Keep commits focused and well-described\n")

	return sb.String()
}

// InitProject generates a CLAUDE.md file in the project directory.
// Returns true if file was created/updated, false if skipped.
func InitProject(projectPath string, force bool) (bool, error) {
	claudeFile := filepath.Join(projectPath, "CLAUDE.md")

	// Check existing file
	if existingData, readErr := os.ReadFile(claudeFile); readErr == nil {
		if !strings.HasPrefix(string(existingData), claudeMDGeneratedHeader) && !force {
			return false, nil // user-owned file, skip
		}
	}

	profile := DetectProjectProfile(projectPath)
	content := GenerateClaudeMD(profile)

	if err := os.WriteFile(claudeFile, []byte(content), 0644); err != nil {
		return false, fmt.Errorf("write CLAUDE.md: %w", err)
	}

	return true, nil
}
