//go:embed bridge_agent_templates.json
var agentTemplatesJSON []byte

// AgentTemplates holds the category-based tool/model mapping (CTO-B1).
type AgentTemplates struct {
	ClaudeCodeVersionMin string                       `json:"claude_code_version_min"`
	Categories           map[string]CategoryTemplate   `json:"categories"`
	RoleOverrides        map[string]RoleOverride       `json:"role_overrides"`
}

// CategoryTemplate defines tools, model, skills, and default risk for a SOUL category.
type CategoryTemplate struct {
	Tools           []string `json:"tools"`
	Model           string   `json:"model"`
	Skills          []string `json:"skills"`
	DefaultRiskMode string   `json:"default_risk_mode,omitempty"` // Sprint 21: UX default, always overridable
}

// RoleOverride allows per-role model overrides.
type RoleOverride struct {
	Model string `json:"model,omitempty"`
}

// LoadAgentTemplates parses the embedded agent templates JSON.
func LoadAgentTemplates() (*AgentTemplates, error) {
	var t AgentTemplates
	if err := json.Unmarshal(agentTemplatesJSON, &t); err != nil {
		return nil, fmt.Errorf("parse agent templates: %w", err)
	}
	return &t, nil
}

const generatedHeader = "# Generated by mtclaw bridge install-agents (claude-code >= 2.x) — do not edit manually"

// InstallAgentsResult reports what happened during install-agents.
type InstallAgentsResult struct {
	Installed int
	Updated   int
	Skipped   int
	Skills    *InstallSkillsResult // nil if skills not installed
}

// InstallAgents generates .claude/agents/{role}.md for each SOUL in soulsDir.
// If roles is non-empty, only those roles are processed.
// Files with the generated header are updated; user-created files are skipped.
func InstallAgents(projectPath, soulsDir string, roles []string, force bool) (*InstallAgentsResult, error) {
	templates, err := LoadAgentTemplates()
	if err != nil {
		return nil, err
	}

	allRoles, err := KnownRoles(soulsDir)
	if err != nil {
		return nil, err
	}

	// Filter roles if specified (CTO-L1)
	targetRoles := allRoles
	if len(roles) > 0 {
		targetRoles = filterRoles(allRoles, roles)
		if len(targetRoles) == 0 {
			return nil, fmt.Errorf("none of the specified roles found in SOUL files")
		}
	}

	agentsDir := filepath.Join(projectPath, ".claude", "agents")
	if err := os.MkdirAll(agentsDir, 0755); err != nil {
		return nil, fmt.Errorf("create agents dir: %w", err)
	}

	result := &InstallAgentsResult{}

	for _, role := range targetRoles {
		soul, err := LoadSOUL(soulsDir, role)
		if err != nil {
			return nil, fmt.Errorf("load SOUL %s: %w", role, err)
		}

		agentFile := filepath.Join(agentsDir, role+".md")

		// CTO-102: Hoist content generation before branch to avoid double call
		content := buildAgentFileContent(soul, templates)

		// Check existing file
		if existingData, readErr := os.ReadFile(agentFile); readErr == nil {
			existingContent := string(existingData)
			if !strings.HasPrefix(existingContent, generatedHeader) && !force {
				// User-created file — don't overwrite (S4)
				result.Skipped++
				continue
			}
			// CTO-101: When force=true, always overwrite (skip hash check)
			if !force {
				existingHash, _ := HashFileContent(agentFile)
				newHash := hashString(content)
				if existingHash == newHash {
					result.Skipped++
					continue
				}
			}
			result.Updated++
		} else {
			result.Installed++
		}
		if err := os.WriteFile(agentFile, []byte(content), 0644); err != nil {
			return nil, fmt.Errorf("write agent file %s: %w", agentFile, err)
		}

		// Write .soul-hash sidecar for stale detection (CTO-105)
		hashFile := filepath.Join(agentsDir, role+".soul-hash")
		_ = os.WriteFile(hashFile, []byte(soul.ContentHash), 0644)
	}

	// Install skills alongside agents (Sprint 20A)
	// CTO-303: Return partial result on skills failure so CLI can report what succeeded.
	skillsResult, err := InstallSkills(projectPath, force)
	if err != nil {
		return result, fmt.Errorf("install skills: %w (agents installed successfully)", err)
	}
	result.Skills = skillsResult

	return result, nil
}

// buildAgentFileContent generates the .claude/agents/{role}.md content.
func buildAgentFileContent(soul *SoulContent, templates *AgentTemplates) string {
	category := soul.Category
	if category == "" {
		category = "executor" // default
	}

	cat, ok := templates.Categories[category]
	if !ok {
		cat = templates.Categories["executor"]
	}

	model := cat.Model
	// Role overrides take precedence
	if override, ok := templates.RoleOverrides[soul.Role]; ok {
		if override.Model != "" {
			model = override.Model
		}
	}

	var sb strings.Builder
	sb.WriteString(generatedHeader + "\n")
	sb.WriteString("---\n")
	sb.WriteString(fmt.Sprintf("name: %s\n", soul.Role))
	sb.WriteString(fmt.Sprintf("description: SOUL persona for %s role\n", soul.Role))
	if len(cat.Tools) > 0 {
		sb.WriteString(fmt.Sprintf("tools: %s\n", strings.Join(cat.Tools, ", ")))
	}
	if model != "" && model != "inherit" {
		sb.WriteString(fmt.Sprintf("model: %s\n", model))
	}
	if len(cat.Skills) > 0 {
		sb.WriteString("skills:\n")
		for _, skill := range cat.Skills {
			sb.WriteString(fmt.Sprintf("  - %s\n", skill))
		}
	}
	sb.WriteString("---\n\n")
	sb.WriteString(soul.Body)
	sb.WriteString("\n\n# Commit this file if the team shares SOUL-aware Claude sessions\n")

	return sb.String()
}

func filterRoles(all, filter []string) []string {
	set := make(map[string]bool, len(filter))
	for _, r := range filter {
		set[r] = true
	}
	var result []string
	for _, r := range all {
		if set[r] {
			result = append(result, r)
		}
	}
	return result
}

func hashString(s string) string {
	h := sha256.Sum256([]byte(s))
	return hex.EncodeToString(h[:])
}
